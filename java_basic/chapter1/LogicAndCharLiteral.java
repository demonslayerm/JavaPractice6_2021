
class LogicAndCharLiteral {

	public static void main(String[] args) {
		System.out.println("論理値リテラル");
		boolean boolValue = true;
		System.out.println("boolean型の論理値: " + boolValue);

		System.out.println("\n文字リテラル");
		char charValue = 'a';
		System.out.println("char型の文字: " + charValue);
		char charUniCodeValue = '\u0061';
		System.out.println("unicodeで表したchar型の文字: " + charUniCodeValue);
	}

}
