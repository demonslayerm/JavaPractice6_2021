
class FloatingPointLiteral {
	public static void main(String[] args) {
		System.out.println("浮動小数リテラル");
		double doubleValue = 3.14;
		System.out.println("double型の小数: " + doubleValue);
		double dLValue = 3.14d;
		System.out.println("double型の小数: " +dLValue);
		float flValue = 3.14f;
		System.out.println("float型の少数: " + flValue);
		double dFactorialValue = 3.14e-2;
		System.out.println("double型の小数: " + dFactorialValue);


	}
}
